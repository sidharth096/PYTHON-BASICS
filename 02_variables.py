# Variables in Python

# Assigning values to variables
a = 10 
name = "Python"
b = 10.5
c = True

# Printing values
print("a =", a)
print("name =", name)
print("b =", b)
print("c =", c)

print("=========================")

# Variable Naming Rules:
# 1. Must start with a letter (a-z, A-Z) or underscore (_)
# 2. Cannot start with a number
# 3. Case sensitive: 'name' and 'Name' are different
# 4. No special characters allowed (like $, %, etc.)

# Multiple variables with same value
x = y = z = 1
print("x =", x, "y =", y, "z =", z)

print("=========================")

# Multiple variable assignments
a, b, c = 1, 2, 3
print("a =", a)
print("b =", b)
print("c =", c)
