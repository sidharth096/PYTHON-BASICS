# Data Types in Python

a = 10          # Integer
name = "Python" # String
b = 10.5        # Float
c = True        # Boolean

# Check data types using type()
print("Type of a:", type(a))        # <class 'int'>
print("Type of name:", type(name))  # <class 'str'>
print("Type of b:", type(b))        # <class 'float'>
print("Type of c:", type(c))        # <class 'bool'>
